; FIBONACCI

DATA SEGMENT
N DB 06H
RESULT DW 00H
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START:
	MOV AX,DATA
	MOV DS,AX
	MOV BL,N		; BL = 05
	MOV AL,00H		; AL = 00H
	MOV CL,01H	 	; CL = 01H
	DEC BL
	DEC BL
REPEAT:
	MOV DL,CL		
	ADD CL,AL		
	MOV AL,DL		
	DEC BL
	JNZ REPEAT
	MOV RESULT,CL
	HLT
CODE ENDS
	END
