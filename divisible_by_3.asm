; DIVISIBLE BY 3

DATA SEGMENT
ARR DB 01H,02H,03H,06H,09H,0CH,0FH
RESULT DB 00H,00H,00H,00H,00H,00H,00H
SIZE DB 07H
DATA ENDS

CODE SEGMENT
	ASSUME DS:DATA,CS:CODE
START:
	MOV AX,DATA
	MOV DS,AX
	MOV SI,OFFSET ARR
	MOV DI,OFFSET RESULT
	MOV CL,SIZE
L:
	MOV AL,[SI]
	MOV AH,00H
	MOV BL,03H
	DIV BL
	CMP AH,00H
	JZ COPY
RETURN:
	INC SI
	DEC CL
	JNZ L
COPY:
	MOV AL,[SI]
	MOV [DI],AL
	INC DI
	JMP RETURN
CODE ENDS
	END