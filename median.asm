DATA SEGMENT
ARR DB 01H,02H,03H,04H,05H
SIZE DB 05H
MEDIAN DB 00H
DATA ENDS

CODE SEGMENT
	ASSUME DS:DATA,CS:CODE
START:
	MOV AX,DATA
	MOV DS,AX
	MOV AL,SIZE
	MOV AH,00H
	MOV BL,02H
	DIV BL
	LEA SI,ARR
L:
	INC SI
	DEC AL
	JNZ L
	MOV AL,[SI]
	MOV MEDIAN,AL
	HLT
CODE ENDS
	END
