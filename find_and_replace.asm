; FIND AND REPLACE THE CHARACTER IN THE STRING

DATA SEGMENT
STR DB 'FIND AND REPLACE THE CHARACTER IN THE STRING HAHA'
COUNT DB 30H
FIND DB 'A'
REPLACE DB 'V'
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA
START:
	MOV AX,DATA
	MOV DS,AX
	MOV SI,OFFSET STR
	MOV CL,COUNT
	MOV CH,00H
	MOV AL,FIND
	MOV BL,REPLACE
RPT:
	CMP AL,[SI]
	JNZ SKIP
	MOV [SI],BL
SKIP:
	INC SI
	LOOP RPT
	HLT
CODE ENDS
	END
