; COUNT THE VOWELS IN THE GIVEN STRING

DATA SEGMENT
STRING DB 'ABCDEFGHIJKLMNOPQRSTUVWXYZ'
COUNT DB 1AH
VOWELS DB 'AEIOU'
NO_OF_VOWELS DB 00H
DATA ENDS

CODE SEGMENT
	ASSUME DS:DATA,CS:CODE
START:
	MOV AX,DATA
	MOV DS,AX
	MOV SI,OFFSET STRING
	MOV CL,COUNT
	MOV CH,00H
	MOV DL,00H	; FOR COUNTING THE VOWELS
L2:
	MOV BL,05H
	MOV DI,OFFSET VOWELS
L1:
	MOV AL,[DI]
	CMP AL,[SI]
	JNZ SKIP
	INC DL
SKIP:
	INC DI
	DEC BL
	JNZ L1
	INC SI
	LOOP L2
	MOV NO_OF_VOWELS,DL
	HLT
CODE ENDS
	END