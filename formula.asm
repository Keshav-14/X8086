; A^2 - B^2

DATA SEGMENT

A DB 21H
B DB 45H
RESULT DB 00H
CARRY DB 00H

DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
	
START:
	MOV AX,DATA
	MOV DS,AX
	MOV BL,00H
	MOV AH,00H
	MOV AL,A
	MUL AL
	MOV CX,AX ; A^2
	MOV AL,B
	MOV AH,00H
	MUL AL	; B^2
	SUB CX,AX
	JNC EXIT
	INC BL
EXIT:
	MOV RESULT,CX
	MOV CARRY,BL
CODE ENDS
	END