; PALINDROME

DATA SEGMENT
STR DB 'MALAYALAM'
SIZE DB 09H
PALINDROME DB 00H
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START:
	MOV AX,DATA
	MOV DS,AX
	LEA SI,STR
	LEA DI,SIZE
	DEC DI
	MOV CL,SIZE
REPEAT:
	MOV AL,[SI]
	CMP AL,[DI]
	JNZ NOT
	INC SI
	DEC DI
	DEC CL
	JNZ REPEAT
	MOV PALINDROME,01H	; PALINDROME STRING
	JMP EXIT
NOT:
	MOV PALINDROME,00H	; NOT A PALINDROME STRING
EXIT:
	HLT
CODE ENDS
	END
