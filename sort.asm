; sorting in descending order

DATA SEGMENT 
ARR DB 11H,22H,33H,44H,55H
SIZE DB 05H
DATA ENDS

CODE SEGMENT
	ASSUME DS:DATA,CS:CODE
START:
	MOV AX,DATA
	MOV DS,AX
	MOV CL,SIZE
	DEC CL
L3:
	LEA SI,ARR
	MOV CH,CL
L2:
	MOV AL,[SI]
	CMP AL,2[SI]
	JNC L1	; USE JC FOR ASCENDING ORDER SORTING
	XCHG AL,2[SI]
	XCHG AL,[SI]
L1:
	INC SI
	DEC CH
	JNZ L2
	DEC CL
	JNZ L3
CODE ENDS
	END
